# Triangle
Base=float(input("Enter Base of Triangle :"))
Height=float(input("Enter Height of Triangle :"))

area1 = 0.5*Base*Height
print(area1)

# Square
Length=float(input("Enter length of square side :"))

area2 = Length * Length
print(area2)

# Rectangle
Width=float(input("Enter Width of Rectangle : "))
Height1=float(input("Enter height of Rectangle : "))

area3 = Width * Height1
print(area3)

# Parallelogram
Base1=float(input("Enter Base of Parallelogram :"))
VerticalHeight = float(input("Enter height of Parallelogram "))

area4 = Base1 * VerticalHeight
print(area4)

# Circle
Radius = float(input("Enter radius of Circle :"))

area5 = 3.1416 * Radius * Radius
print(area5)


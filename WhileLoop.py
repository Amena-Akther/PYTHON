# Program to print 1 to 10 using while loop
i=1
while i<=10:     #The while loop will iterate until condition becomes false.
    print(i,end=" ")
    i=i+1
print("program End")

# Program to print even number
j=2
while j<=20:
    print(j)
    j=j+2
print("program End")
# Program to print odd number
k=1
while k<=10:
    print(k)
    k=k+2
print("program End")

# program to print sum of n number
n=int(input("Enter the last term :"))
sum=0
x=1
while x<=n:
    sum = sum + x
    x=x+1
print(sum)